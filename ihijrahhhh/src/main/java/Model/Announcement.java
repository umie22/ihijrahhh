package Model;

public class Announcement {
	
	private int Announcement_ID;
	private String Announcement_Detail;
	private String Announcement_Date;

	
	public int getAnnouncement_ID() {
		return Announcement_ID;
	}
	public void setAnnouncement_ID(int announcement_ID) {
		Announcement_ID = announcement_ID;
	}
	public String getAnnouncement_Detail() {
		return Announcement_Detail;
	}
	public void setAnnouncement_Detail(String announcement_Detail) {
		Announcement_Detail = announcement_Detail;
	}
	public String getAnnouncement_Date() {
		return Announcement_Date;
	}
	public void setAnnouncement_Date(String announcement_Date) {
		Announcement_Date = announcement_Date;
	}



}
